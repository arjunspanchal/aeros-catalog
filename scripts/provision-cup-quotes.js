#!/usr/bin/env node
/**
 * One-shot: create the "Cup Quotes" table in the calculator base.
 * Run: node --env-file=.env.local scripts/provision-cup-quotes.js
 *
 * Requires AIRTABLE_TOKEN with schema.bases:read + schema.bases:write
 * scopes on the AIRTABLE_CALC_BASE_ID base. Idempotent — if the table
 * already exists, it adds any missing fields rather than failing.
 */

const TOKEN = process.env.AIRTABLE_PAT_CALCULATOR || process.env.AIRTABLE_TOKEN;
const BASE = process.env.AIRTABLE_CALC_BASE_ID;
const TABLE_NAME = process.env.AIRTABLE_CUP_QUOTES_TABLE || "Cup Quotes";

if (!TOKEN || !BASE) {
  console.error("Set AIRTABLE_PAT_CALCULATOR (or legacy AIRTABLE_TOKEN) and AIRTABLE_CALC_BASE_ID in .env.local");
  process.exit(1);
}

const API = `https://api.airtable.com/v0/meta/bases/${BASE}/tables`;

async function listExisting() {
  const res = await fetch(API, { headers: { Authorization: `Bearer ${TOKEN}` } });
  if (!res.ok) throw new Error(`list failed: ${res.status} ${await res.text()}`);
  const data = await res.json();
  return data.tables || [];
}

async function createTable(def) {
  const res = await fetch(API, {
    method: "POST",
    headers: { Authorization: `Bearer ${TOKEN}`, "Content-Type": "application/json" },
    body: JSON.stringify(def),
  });
  const body = await res.text();
  if (!res.ok) throw new Error(`Create ${def.name} failed: ${res.status} ${body}`);
  return JSON.parse(body);
}

async function addField(tableId, field) {
  const res = await fetch(`${API}/${tableId}/fields`, {
    method: "POST",
    headers: { Authorization: `Bearer ${TOKEN}`, "Content-Type": "application/json" },
    body: JSON.stringify(field),
  });
  const body = await res.text();
  if (!res.ok) throw new Error(`Add field ${field.name} failed: ${res.status} ${body}`);
  return JSON.parse(body);
}

// Field definitions in Airtable Meta API format. The "Quote Ref" column is
// the primary field and gets created with the table; everything else is added
// after. Order doesn't matter for the app — code references fields by name.
const PRIMARY_FIELD = {
  name: "Quote Ref",
  type: "singleLineText",
};

const FIELDS = [
  { name: "Quote Date", type: "date", options: { dateFormat: { name: "iso" } } },
  { name: "Wall Type", type: "singleSelect", options: { choices: [
    { name: "Single Wall" }, { name: "Double Wall" }, { name: "Ripple" },
  ] } },
  { name: "Size", type: "singleSelect", options: { choices: [
    { name: "8oz" }, { name: "12oz" }, { name: "16oz" }, { name: "20oz" },
  ] } },
  { name: "SKU", type: "singleLineText" },
  { name: "Inner GSM", type: "number", options: { precision: 0 } },
  { name: "Outer GSM", type: "number", options: { precision: 0 } },
  { name: "Inner Coating", type: "singleSelect", options: { choices: [
    { name: "None" }, { name: "PE" }, { name: "2PE" }, { name: "Aqueous" }, { name: "PLA" },
  ] } },
  { name: "Sidewall Rate INR/kg", type: "number", options: { precision: 2 } },
  { name: "Bottom Rate INR/kg", type: "number", options: { precision: 2 } },
  { name: "Outer Rate INR/kg", type: "number", options: { precision: 2 } },
  { name: "Print Method", type: "singleSelect", options: { choices: [
    { name: "No printing" }, { name: "Flexo" }, { name: "Offset" },
  ] } },
  { name: "Outer Print Method", type: "singleSelect", options: { choices: [
    { name: "No printing" }, { name: "Flexo" }, { name: "Offset" },
  ] } },
  { name: "Plain/Printed", type: "singleSelect", options: { choices: [
    { name: "Plain" }, { name: "Printed" },
  ] } },
  { name: "Colours", type: "number", options: { precision: 0 } },
  { name: "Coverage %", type: "number", options: { precision: 0 } },
  { name: "Case Pack", type: "number", options: { precision: 0 } },
  { name: "Order Qty", type: "number", options: { precision: 0 } },
  { name: "Margin %", type: "number", options: { precision: 2 } },
  { name: "Mfg Cost INR", type: "number", options: { precision: 4 } },
  { name: "Selling Price INR", type: "number", options: { precision: 2 } },
  { name: "Cost per Case INR", type: "number", options: { precision: 2 } },
  { name: "Order Total INR", type: "number", options: { precision: 2 } },
  { name: "Cup Weight g", type: "number", options: { precision: 2 } },
  { name: "One-time Plate/Die INR", type: "number", options: { precision: 0 } },
  { name: "Generated By", type: "singleLineText" },
  { name: "Client Email", type: "email" },
  { name: "Notes", type: "multilineText" },
];

(async () => {
  console.log(`Provisioning "${TABLE_NAME}" in base ${BASE}…`);

  const existing = await listExisting();
  let table = existing.find((t) => t.name === TABLE_NAME);

  if (!table) {
    console.log("  Creating table…");
    table = await createTable({
      name: TABLE_NAME,
      description: "Saved cup quotes from the rate calculator (admin + client).",
      fields: [PRIMARY_FIELD],
    });
    console.log(`  ✓ Created table ${table.id}`);
  } else {
    console.log(`  Table exists (${table.id}). Adding any missing fields…`);
  }

  const presentFieldNames = new Set((table.fields || []).map((f) => f.name));

  for (const f of FIELDS) {
    if (presentFieldNames.has(f.name)) {
      console.log(`  (exists) ${f.name}`);
      continue;
    }
    try {
      await addField(table.id, f);
      console.log(`  + ${f.name}`);
    } catch (e) {
      console.error(`  ✗ ${f.name}: ${e.message}`);
    }
  }

  console.log("Done. The Save Quote button on the cup calculator should now work.");
})().catch((e) => {
  console.error(e.message || e);
  process.exit(1);
});
